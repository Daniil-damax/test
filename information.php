                    <h2>Закрыть ИП с долгами можно!</h2>
                    
                    <p>Ранее закрытие ИП не производилось без погашения долгов и предоставления в налоговую справки из пенсионного фонда об отсутствии задолженности. Теперь, в случае если Вы не предоставите справку из ПФР, налоговая самостоятельно получит данные сведения путём межведомственного запроса в территориальный орган Пенсионного фонда РФ в электронной форме (ст. 22.3 ФЗ N 129-ФЗ). Таким образом, непредоставление справки из ПФР при подаче заявления на закрытие ИП не является осованием для отказа в государственной регистрации. Однако, если имеется задолженность, то следует понимать, что она никуда не исчезнет, и после закрытия ИП будет числиться за Вами, как за физическим лицом. Внимание! - Если у ИП были сотрудники, то перед подачей в налоговую заявления о прекращении деятельности ИП всё же необходимо обратиться в ПФР и ФСС для сдачи отчетности и уплаты взносов. - Получить справку из ПФР и урегулировать вопросы задолженности по взносам можно в территориальном управлении Пенсионного фонда по месту жительства ИП. Адреса территориальных управлений можно уточнить на официальном сайте ПФР.</p>
                    
